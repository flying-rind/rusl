/// 每个 hex 值的辅助宏：在 const 上下文中用 `.to_be()` 实现 C 的 `X(x)` 宏语义。
///
/// - 大端平台: `x.to_be()` = `x`（不变）
/// - 小端平台: `x.to_be()` = `x.swap_bytes()`（字节交换，等价于 C 的 `X(x)`）
///
/// 这确保了无论平台字节序如何，表中每个 `u16` 的内存布局都与 C 的实现一致。
#[allow(non_snake_case)]
const fn X(x: u16) -> u16 {
    x.to_be()
}

