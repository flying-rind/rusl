//! __ctype_b_loc — 返回 C locale 字符分类位掩码表的地址。
//! 对应 musl src/ctype/__ctype_b_loc.c
//!
//! 该函数被 `<ctype.h>` 中的宏（如 `isalpha(c)` 等）用于 O(1) 字符分类查询。
//! 返回指向内部静态查找表的指针，表中每个元素为 `c_ushort` 类型的位掩码，
//! 编码了对应字符的分类属性（是否为字母、数字、空白等）。

#![allow(unused_imports, unused_variables)]

use core::ffi::c_ushort;

/// 内部辅助类型: 包裹裸指针用于 static 存储。
///
/// `*const T` 未实现 `Sync`，无法直接用于 `static` 声明。
/// 此包装类型安全实现 `Sync`，因为封装的指针指向编译时常量
/// 只读数据，多线程并发读取是安全的。
struct SyncPtr<T: 'static>(*const T);
// SAFETY: 封装的指针仅指向只读常量数据，永不改变，线程安全。
unsafe impl<T: 'static> Sync for SyncPtr<T> {}

