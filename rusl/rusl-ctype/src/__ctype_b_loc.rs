/// 偏移后的表指针（指向 `CTYPE_TABLE[128]`）。
///
/// C 等价: `static const unsigned short *const ptable = table+128;`
/// 使用 `usize` 存储以避免 `*const c_ushort` 不满足 `Sync` 约束的问题。
/// `usize` 与指针宽度相同（32-bit 上 4 字节，64-bit 上 8 字节），
/// 强制转为 `*const *const c_ushort` 后语义等价。

// ============================================================================
// 公开导出接口 (C ABI 兼容)
// ============================================================================

/// 返回 C locale 字符分类位掩码表的指针的指针。
///
/// 解引用一次得到表指针 `table_ptr`，该表有 384 个 `c_ushort` 元素，
/// 索引偏移量为 +128（即 `table_ptr[-128]` 到 `table_ptr[255]` 有效）。
/// 实际使用时，调用者以字符码位为索引直接查询：`(*table_ptr)[c]` 得到字符 `c` 的分类掩码。
///
/// # 返回值
///
/// 返回 `*const *const c_ushort`，指向内部静态字符分类表指针。
/// 返回的指针在整个程序生命周期内有效，指向只读数据段。
///
/// # 表结构
///
/// 内部静态表 `table` 的组织方式：
/// - `table[0..127]`: 索引 -128 到 -1 的条目（全为零，实际不使用）
/// - `table[128..255]`: 索引 0 到 127 的条目（ASCII 范围）
/// - `table[256..383]`: 索引 128 到 255 的条目（扩展 ASCII 范围）
///
/// 内部返回指针 `ptable = &table[128]`，即 `ptable[-128]` 对应 `table[0]`。
///
/// 表内容按字节序（大端/小端）自动调整：使用 `u16::to_be()` 实现 C 的 `X(x)` 宏语义，
/// 确保不同平台上的内存布局一致。
///
/// # Safety
///
/// 此函数始终返回有效指针，调用者无需满足特殊前置条件。标记为 `unsafe` 是因为：
/// - 返回原始指针，调用者通过该指针访问表内容时需自行保证索引在 [-128, 255] 范围内。
/// - 表内容为只读，不可通过返回的指针修改。
///
/// # 不变量
///
/// 纯函数，始终返回同一常量指针。无内部可变状态。表内容永不改变。
///
/// # 复杂度
///
/// 时间复杂度 O(1)。
#[no_mangle]
pub unsafe extern "C" fn __ctype_b_loc() -> *const *const c_ushort {
    // 使用 SyncPtr 包裹裸指针以满足 Sync 约束
    static PTR: SyncPtr<c_ushort> = SyncPtr(CTYPE_TABLE.as_ptr().wrapping_add(128));
    &raw const PTR.0 as *const *const c_ushort
}
