/// 测试宽字符 wc 是否为 Unicode 标点符号。
///
/// 使用编译时生成的位图数据进行 O(1) 查找。覆盖码点范围 0..0x20000。
/// 对于 `wc >= 0x20000` 的字符，直接返回 0。
///
/// # 参数
///
/// * `wc` - 宽字符值，类型为 `wint_t` (`c_uint`)，任意宽字符值（含 `WEOF`）
///
/// # 返回
///
/// * 如果 wc 是标点符号，返回非零值（musl 中为 1）
/// * 如果 wc 不是标点符号或 `wc >= 0x20000`，返回 0
///
/// # 系统算法
///
/// 二级位图查找:
/// - `wc < 0x20000` 时: `table[wc>>8]` 获取二级表偏移，然后定位到位图的对应位
/// - `wc >= 0x20000` 时: 直接返回 0
///
/// 时间复杂度 O(1)，依赖编译时生成的 punct.h 位图数据。
///
/// # Safety
///
/// 此函数标记为 unsafe 以匹配 C ABI 调用约定。函数本身无 unsafe 操作。
#[no_mangle]
pub unsafe extern "C" fn iswpunct(wc: wint_t) -> c_int {
    __iswpunct_l(wc, core::ptr::null_mut())
}

/// 测试宽字符 c 是否为 Unicode 标点符号（带 locale 参数）。
///
/// `locale` 参数在当前 musl 实现中未实际使用（Unicode 标点判定不依赖 locale），
/// 保留为 API 兼容占位。行为与 `iswpunct(wc)` 完全一致。
///
/// # 参数
///
/// * `c` - 宽字符值
/// * `l` - locale 句柄，当前实现中忽略。可为 `core::ptr::null_mut()`
///
/// # Safety
///
/// `l` 若不为 null 则必须指向有效的 locale 对象。
#[no_mangle]
pub unsafe extern "C" fn iswpunct_l(c: wint_t, l: locale_t) -> c_int {
    __iswpunct_l(c, l)
}

/// iswpunct 和 iswpunct_l 的内部委托实现。
///
/// 执行二级位图查找:
/// - `wc < 0x20000` 时在位图中查找对应位
/// - `wc >= 0x20000` 时直接返回 0
///
/// `locale` 参数在当前实现中忽略，保留为 API 兼容占位。
///
/// `iswpunct(wc)` 等价于 `__iswpunct_l(wc, core::ptr::null_mut())`
/// `iswpunct_l(c, l)` 等价于 `__iswpunct_l(c, l)`
pub(crate) fn __iswpunct_l(c: wint_t, _l: locale_t) -> c_int {
    if c < 0x20000 {
        let hi = (c >> 8) as usize;
        let idx = PUNCT_TABLE[hi] as usize;
        // idx==0 表示该高字节范围无标点符号，直接返回 0
        if idx == 0 {
            return 0;
        }
        // Level-2 位图查找: table[table[wc>>8]*32 + ((wc&255)>>3)] >> (wc&7) & 1
        let offset = idx * 32 + ((c as usize & 255) >> 3);
        let bit = c as usize & 7;
        ((PUNCT_TABLE[offset] >> bit) & 1) as c_int
    } else {
        0
    }
}
