//! isascii — 测试字符是否为 7 位 ASCII 字符。
//! 对应 musl src/ctype/isascii.c
//!
//! 这是 BSD/POSIX 兼容函数。现代代码中通常不需要此函数
//! （直接检查 `c <= 0x7f` 即可），但为兼容性需要提供。

#![allow(unused_imports, unused_variables)]

use core::ffi::c_int;

/// 测试字符 c 是否为 7 位 ASCII 字符（码位在 0 到 127 范围内）。
///
/// # 参数
///
/// - `c`: 待测试的字符，类型为 `c_int`。任意整数值均可，无其他前置条件。
///
/// # 返回值
///
/// - 若 `c` 在 0 到 127 范围内（7 位 ASCII 字符）：返回非零值（通常为 1）。
/// - 若 `c` 超出 0-127 范围（bit 7 或更高位被设置），或 `c` 为负数：返回 0。
///
/// # Safety
///
/// 标记为 `unsafe` 以保持与 C ABI 兼容。调用者无特殊前置条件，任意 `c_int` 值均可安全调用。
///
/// # 算法
///
/// 使用等价位运算 `(c & !0x7f) == 0` 或 `(c as u32) <= 0x7f`：
/// - 检查 c 的高位（bit 7 及以上）是否全为零
/// 时间复杂度 O(1)，无分支。
///
/// # 不变量
///
/// 纯函数。无内部状态，无 locale 依赖。
///
/// # 平台说明
///
/// 此函数行为在所有平台上完全一致，不依赖字节序或 locale。
#[no_mangle]
pub unsafe extern "C" fn isascii(c: c_int) -> c_int {
    // 算法: !(c & ~0x7f)
    // 检查 c 的 bit 7 及以上是否全为零，即 c 是否在 0-127 范围内。
    // 若 c & ~0x7f == 0，说明 c 是 7 位 ASCII，返回 1（真）；
    // 否则返回 0（假）。
    // 时间复杂度 O(1)，无分支。
    if (c & !0x7f) == 0 { 1 } else { 0 }
}
