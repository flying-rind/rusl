/// ISO C 标准库: 确定宽字符在终端显示时占用的列数。
///
/// **前置条件**:
/// - `wc`: 类型为 `wchar_t`（`i32`），Unicode 码点值。
///
/// **后置条件**:
/// - Case 1: `wc` 是 null 字符 (`L'\0'`) -> 返回 0。
/// - Case 2: `wc` 是可打印字符
///   - `wc < 0xff` 且 `(wc+1 & 0x7f) >= 0x21` (ASCII 可打印, 0x20-0x7E)：返回 1
///   - `wc` 是 BMP 普通宽度字符：返回 1
///   - `wc` 是宽字符（CJK 等，在 `WIDE_TABLE` 位图中）：返回 2
/// - Case 3: `wc` 是组合字符（nonspacing mark，在 `NS_TABLE` 位图中）-> 返回 0。
/// - Case 4: `wc` 是不可打印字符
///   - `wc` 在 `0x01`-`0x1F` 或 `0x7F`-`0x9F` 范围内（C0/C1 控制字符）：返回 -1
///   - `(wc & 0xfffe) == 0xfffe`（非字符码点如 U+FFFE/U+FFFF）：返回 -1
///   - `U+E0001` 或 `U+E0020`-`U+E00EF` 范围内的标记字符：返回 0
///
/// **不变量**: 纯函数。`NS_TABLE` 和 `WIDE_TABLE` 为编译时常量，运行时只读。线程安全。
///
/// [ISO C 标准库 `<wchar.h>`]
#[no_mangle]
pub unsafe extern "C" fn wcwidth(wc: wchar_t) -> c_int {
    // musl 原实现的逐行翻译
    let wc_u = wc as u32;

    // Phase 1: wc < 0xff -> ASCII/控制字符
    // C: if (wc < 0xffU) return (wc+1 & 0x7f) >= 0x21 ? 1 : wc ? -1 : 0;
    if wc_u < 0xff {
        if ((wc_u + 1) & 0x7f) >= 0x21 {
            return 1;
        }
        if wc_u != 0 {
            return -1;
        }
        return 0;
    }

    // Phase 2: BMP and below
    // C: if ((wc & 0xfffeffffU) < 0xfffe) { ... }
    if (wc_u & 0xfffeffff) < 0xfffe {
        // 位图查找辅助闭包
        let bitmap_test = |table: &[u8], cp: u32| -> bool {
            let high = (cp >> 8) as usize;
            let low = (cp & 255) as usize;
            // 一级表保护
            if high < table.len() {
                let base = table[high] as usize;
                let byte_idx = base * 32 + (low >> 3);
                if byte_idx < table.len() {
                    let bit_idx = low & 7;
                    (table[byte_idx] >> bit_idx) & 1 != 0
                } else {
                    false
                }
            } else {
                false
            }
        };

        // 检查是否为组合字符 (nonspacing mark)
        if bitmap_test(NS_TABLE, wc_u) {
            return 0;
        }
        // 检查是否为宽字符 (CJK 等)
        if bitmap_test(WIDE_TABLE, wc_u) {
            return 2;
        }
        return 1;
    }

    // Phase 3: 非字符码点
    // C: if ((wc & 0xfffe) == 0xfffe) return -1;
    if (wc_u & 0xfffe) == 0xfffe {
        return -1;
    }

    // Phase 4: CJK Extension B (U+20000 - U+3FFFF)
    // C: if (wc-0x20000U < 0x20000) return 2;
    if wc_u.wrapping_sub(0x20000) < 0x20000 {
        return 2;
    }

    // Phase 5: 特殊标记字符
    // C: if (wc == 0xe0001 || wc-0xe0020U < 0x5f || wc-0xe0100U < 0xef) return 0;
    if wc_u == 0xe0001
        || wc_u.wrapping_sub(0xe0020) < 0x5f
        || wc_u.wrapping_sub(0xe0100) < 0xef
    {
        return 0;
    }

    // 默认: 普通宽度字符
    1
}
