/// 简单 ASCII 大小写映射回退。
///
/// 在完整 casemap 数据表不可用时（表为空或索引越界），
/// 此函数处理 ASCII 字母的大小写转换。
///
/// - `dir == 0` (tolower): 'A'-'Z' -> 'a'-'z'
/// - `dir == 1` (toupper): 'a'-'z' -> 'A'-'Z'
#[inline]
fn ascii_casemap(wc: wint_t, dir: c_int) -> wint_t {
    if dir == 0 {
        // tolower: 'A' (0x41) .. 'Z' (0x5A) -> 'a' (0x61) .. 'z' (0x7A)
        if wc >= b'A' as wint_t && wc <= b'Z' as wint_t {
            wc + 32
        } else {
            wc
        }
    } else {
        // toupper: 'a' (0x61) .. 'z' (0x7A) -> 'A' (0x41) .. 'Z' (0x5A)
        if wc >= b'a' as wint_t && wc <= b'z' as wint_t {
            wc - 32
        } else {
            wc
        }
    }
}

/// casemap 查询常量 mt[]，用于二级基-6 表分类。
const MT: [usize; 3] = [2048, 342, 57];

