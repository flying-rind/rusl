/// 核心大小写映射引擎。
///
/// 算法流程 (对应 musl casemap):
/// 1. Phase 1: `wc >= 0x20000` -> 直接返回 `wc`（CJK Extension B 及以上无大小写）
/// 2. Phase 2: 尝试通过二级基-6 表执行完整 Unicode 映射
/// 3. 回退: 若数据表不可用（表为空或索引越界），使用简单 ASCII 映射
///
/// **参数**:
/// - `wc`: 输入宽字符
/// - `dir`: 映射方向，0 = 转小写，1 = 转大写
///
/// **时间复杂度**: O(log n) 最坏情况，通常 O(1)。
///
/// Rust 设计: 使用 `pub` 可见性以便集成测试访问，
/// 但不使用 `#[no_mangle]`，不会作为 C ABI 符号导出。
pub fn casemap(wc: wint_t, dir: c_int) -> wint_t {
    // WEOF: 直接返回
    if wc == WEOF {
        return WEOF;
    }

    // Phase 1: CJK Extension B 及以上无大小写映射
    if wc >= 0x20000 {
        return wc;
    }

    // Phase 2-5: 尝试完整 Unicode casemap 算法
    // 若数据表可用则运行完整算法，否则回退到简单 ASCII 映射
    let b = (wc >> 8) as usize;
    let c = (wc & 255) as usize;

    // 检查一级表是否可用
    if b < CASEMAP_TAB.len() {
        let level1_idx = CASEMAP_TAB[b] as usize;
        let x = c / 3;
        let y = c % 3;

        // 计算二级表索引: tab[b] * 86 + x
        let level2_idx = level1_idx * 86 + x;
        if level2_idx < CASEMAP_TAB.len() {
            let mut v = CASEMAP_TAB[level2_idx] as usize;

            // 基-6 分类: v = (v * mt[y] >> 11) % 6
            v = (v * MT[y] >> 11) % 6;

            // 查询规则: rules[rulebases[b] + v]
            if b < CASEMAP_RULEBASES.len() {
                let rule_idx = CASEMAP_RULEBASES[b] as usize + v;
                if rule_idx < CASEMAP_RULES.len() {
                    let r = CASEMAP_RULES[rule_idx] as i32;
                    let rt = r & 255;
                    let rd = r >> 8;

                    // Phase 4: rt < 2 -> 简单 delta 映射
                    if rt < 2 {
                        // 与 C 原实现等价的位运算:
                        //   c0 + (rd & -(rt ^ dir))
                        let delta = rd & -(rt ^ dir);
                        return ((wc as i32).wrapping_add(delta)) as u32;
                    }

                    // Phase 5: 复杂映射时二分查找异常表
                    // 仅在异常表可用时执行
                    if !CASEMAP_EXCEPTIONS.is_empty() {
                        let mut xn = (rd & 0xff) as usize;
                        let mut xb = ((rd as usize) >> 8) & 0xff;
                        while xn > 0 {
                            let try_idx = xb + xn / 2;
                            if try_idx < CASEMAP_EXCEPTIONS.len() {
                                let exceptional = CASEMAP_EXCEPTIONS[try_idx] as usize;
                                let try_c = (exceptional & 0xff) as usize;
                                if try_c == c {
                                    let ex_rule = CASEMAP_EXCEPTIONS[try_idx] as u32;
                                    let ex_rule_idx = (ex_rule >> 8) as usize;
                                    if ex_rule_idx < CASEMAP_RULES.len() {
                                        let ex_r = CASEMAP_RULES[ex_rule_idx] as i32;
                                        let ex_rt = ex_r & 255;
                                        let ex_rd = ex_r >> 8;
                                        if ex_rt < 2 {
                                            let delta = ex_rd & -(ex_rt ^ dir);
                                            return ((wc as i32).wrapping_add(delta)) as u32;
                                        }
                                        // 硬编码标题大小写 (hard-coded titlecase)
                                        return ((wc as i32).wrapping_add(if dir != 0 { -1 } else { 1 })) as u32;
                                    }
                                    return wc;
                                } else if try_c > c {
                                    xn /= 2;
                                } else {
                                    xb = try_idx;
                                    xn -= xn / 2;
                                }
                            } else {
                                break;
                            }
                        }
                    }
                    // 未在异常表中找到：返回原值
                    return wc;
                }
            }
        }
    }

    // 回退: 简单 ASCII 映射
    ascii_casemap(wc, dir)
}
