/// ISO C 标准库: 将宽字符转换为小写。
///
/// **前置条件**: `wc` 为任意宽字符值（含 `WEOF`）。
///
/// **后置条件**:
/// - 若 `wc` 存在小写映射：返回对应的小写字符。
/// - 若 `wc` 不存在小写映射或 `wc == WEOF`：返回 `wc` 本身。
///
/// **不变量**: 纯函数，无副作用，线程安全。
///
/// [ISO C 标准库 `<wctype.h>`]
